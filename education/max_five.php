      <section class="our-services" id="4-5">
        <div class="row session-title">
          <h2>Feeding & Nutrition Tips: 4-to 5-Year-Olds</h2>
          
        </div>
        <div class="container">
          <div class="row">
            <div class="col-md-12 col-sm-12">
             
              <div class="service-box">
                <div class="d-flex">
                  <div class="det">
                    <p>Children feel better when they eat well. During the preschool and kindergarten years, your child should be eating the same foods as the rest of the family.

                        Your job as a parent is to offer foods with nutritional value in a calm environment and to have regular times for eating. Your child's job is to decide whether he or she is hungry and how much food to eat when it's offered.</p>
                    <h4 class="" style="color: green">8 Tips for Parents:</h4>

                    <p>
                       <b> Offer a range of healthy foods.</b> When children eat a variety of foods, they get a balance of the vitamins they need to grow. Healthy options include fresh vegetables and fruits, low-fat dairy products (milk, yogurt, cheeses) or dairy substitutes, lean proteins (beans, chicken, turkey, fish, lean hamburger, tofu, eggs), and whole-grain cereals and bread. <br>
                        
                       <b> Don't expect children to "clean their plates."</b> Serve appropriate portion sizes, but do not expect your child to always eat everything served. Even better, let your children choose their own portion sizes. It is okay if children do not eat everything on their plates. At this age, they should learn to know when they are full. Some four-year-olds may still be picky eaters. Parents can encourage their children to try new foods, but they should not pressure eating.
                        <br>
                        <b> Offer regular meal times and sit together.</b> Serve foods at regular meal and snack times. Try to be careful to not offer foods between these eating times. Children who are eating or "grazing" throughout the day may not be hungry at mealtimes, when healthier foods tend to be available. When it is meal or snack time, turn off the TV, and eat together at the table. This helps create a calm environment for eating. 
                        <br>
                        <b>Limit processed food and sugary drinks.</b> Another parent role is to limit how much processed food is in the house and to limit fast food. Most important is to limit sugary drinks. Sugary drinks include soda, juice drinks, lemonade, sweet tea, and sports drinks. Sugary drinks can lead to cavities and unhealthy weight gain.
                        <br>
                        <b>  The best drinks are water and milk.</b> The best drinks for children are water and milk (including non-dairy milk). Milk provides calcium and vitamin D to build strong bones. Ice cream is okay once in a while, but it should not be offered every day. Whole fruit is preferable to fruit juice—even if it is 100% juice—as juice is a concentrated source of sugar and low in fiber. If you offer juice, make it 100% fruit juice and limit it to 4 oz. or less per day. It is best to serve juice with a meal, as juice is more likely to cause cavities when served between meals.
                        <br>
                        <b>Small portions for small children.</b> It is important to pay attention to portion sizes. Four- and five-year-olds need smaller servings than adults. Encourage your children to choose their own serving size, but use smaller plates, bowls, and cups. See Energy In: Recommended Food & Drink Amounts for Children.
                        <br>
                        <b> Turn off the TV—especially at mealtimes.</b> Television advertising can be a big challenge to your child's good nutrition. Four- and five-year-olds are easily influenced by ads for unhealthy foods like sugary cereals, fast food, and sweets. The best way to avoid this is put in place a "media curfew" at mealtime and bedtime, putting all devices away or plugging them into a charging station for the night.
                        <br>
                        <b> Teach table manners.</b> At this age, your child should be ready to learn basic table manners. By age four, he or she will no longer grip the fork or spoon in his or her fist and be able to hold them like an adult. With your help, he or she can begin learning the proper use of a table knife. You can also teach other table manners, such as not talking with a full mouth, using a napkin, and not reaching across another person's plate. While it's necessary to explain these rules, it's much more important to model them. Your child will watch to see how the rest of the family is behaving and follow their lead. It's easier to develop table manners if you have a family custom of eating together. Make at least one meal a day a special and pleasant family time. Have your child set the table or help in some other way in preparing the meal.</p>
