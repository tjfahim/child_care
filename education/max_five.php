          <h2>Feeding your child at 1–2 years</h2>
          
        </div>
        <div class="container">
          <div class="row">
            <div class="col-md-12 col-sm-12">
             
              <div class="service-box">
                <div class="d-flex">
                  <div class="det">
                    <b>At 1 year, solid foods – including healthy snacks – are now your child’s main source of energy and nutrition.</b> <br>
                    <b>He can take between three quarters to one cup of food three to four times a day, plus one to two snacks between meals.</b><br>
                    <b>Continue breastfeeding as much as your child wants, until he is at least 2 years old.</b><br>
                    <b>Avoid junk food and soft drinks.</b><br>
                    <p>At this age, breastmilk still provides important nutrition and protection against disease, but other foods become her main source of nutrition and energy. Feed her other foods first and then breastfeed after if she is still hungry.</p><br>
                    <h4 class="" style="color: green">What to feed your child</h4>

                    <p>
                        Your child can eat anything, so give her some of all the food your family eats and make every bite count. Each meal needs to be packed with nutritious food. <br>

                        Be sure she has a portion of animal foods (milk, dairy, eggs, meat, fish and poultry) each day, plus legumes (like chickpeas, lentils or peas) – or nuts, and orange or green vegetables and fruits. Add a little oil or fat to her food for energy.<br>
                        
                        Be sure your child’s snacks are healthy, such as fresh fruit.
                    </p>
                    <h4 class="" style="color: green"> How much food and how often</h4>

                    <p>
                       
                        Your child can take between three quarters to one cup of food three to four times a day, plus one to two snacks between meals.<br>
                        
                        If you’re not breastfeeding, he’ll need to eat more often. At 1 year, about the time he’s starting to walk, your child's feeding schedule should include four to five meals a day, plus two healthy snacks. Milk products are a very important part of your child’s diet – give him one or two cups of milk a day.
                    </p>
                    <h4 class="" style="color: green"> Foods to avoid</h4>

                    <p>
                       
                        Avoid junk food and soft drinks. Factory-made snacks like crisps, cookies, cakes, soda and candy are unhealthy. They have high amounts of sugar, salt, fat and chemicals, and take up space in your child’s stomach that should be filled with nutritious foods.
                    </p>
                    <h4 class="" style="color: green">Mealtime tips</h4>

                    <p>
                        
Having his own bowl of food will help your child learn to feed himself. Start as soon as he wants. Give him all the food he needs and plenty of time to eat. <br>

At first, he’ll be slow and messy. Help him so that he gets most of the food in his mouth (instead of on himself or the floor!). Encourage him to finish it and make sure he has had enough. <br>

Give your child lots of love and encouragement to eat during meal times.<br>

Sit in front of him and make eye contact. Interact with your child, smile at him, talk to him and praise him for eating.

Make the meal a happy time!
                    </p>
                    <h4 class="" style="color: green"> What to do when your child refuses to eat solid foods</h4>

                    <p>
                       
Make sure she is hungry at mealtimes and has not just had a snack. Although breastfeeding continues to be healthy for your child, breastfeed her only after her meal. At this age, she should eat solid food first. 
<br>
Give your child healthy food that she likes or mix the food she likes with food she doesn’t like as much. Try different food combinations and textures.
<br>
If she still refuses, don’t force or pressure her to eat, and don’t be tempted to give her junk food instead.
<br>
Be calm and accepting. Give your child positive attention when she does eat, but don’t make it a problem when she doesn’t eat. Just take the food away, cover it, and offer it to her again a bit later.
                    </p>
                   
