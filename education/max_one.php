              <div class="d-flex">
                <div class="det">
                  <h4 class="" style="color: green">
                    AGE/STAGE: Babies 4-6 Months
                  </h4>

                  <p>
                    The AAP recommends that an infant not be started on solid
                    foods until after 6 months of age. Many pediatricians still
                    start babies on solids around 4 months of age. This chart
                    accommodates all ages and stages up to 12 months.
                    <br /><br />
                    <b>CEREALS</b>

                    Rice and Oatmeal cereals are the least of the allergenic
                    grains and thus most babies are started out with those
                    cereals. You don’t HAVE to start with cereal – try avocado
                    instead or banana.<br />
                    <b>FRUITS</b>

                    May be served raw after 8 months old or earlier if the
                    fruits are soft and baby does not have digestion troubles –
                    bananas and avocados do NOT need to be cooked ever.<br />
                    <b>VEGGIES</b>

                    Always serve cooked until after 12 months old or when baby
                    can chew well enough so that no choking hazard is
                    present.<br />
                    <b>PROTEIN</b>

                    Always serve cooked with no pink areas – NEVER give a small
                    baby/child raw meat or fish<br />
                    <b>DAIRY</b>

                    NEVER replace breast milk or formula until after 12 months
                    of age – serious health risks are possible. Never give a
                    child under the age of 2yrs old low fat or skim milk
                    products; whole milk is necessary.<br /><br />

                    When thinking about starting baby on solid foods and
                    introducing solids to baby, a good rule of thumb is to
                    “Watch the Baby – Not the Calendar”. This is true when
                    introducing solid foods (complementary foods) for both
                    breastfed and formula fed infants. Just because baby has
                    turned 4 months old does not mean she must be introduced to
                    solid foods. <br /><br />

                    <b>
                      How much will your 4 month – 6 month old baby eat at his
                      first meal?</b
                    ><br />
                    Babies will probably only eat 1/2 of a tablespoon portion of
                    food the very first times you begin solids. Don’t expect
                    your baby to “finish” a meal; remember this is a new
                    experience for your baby. As your baby gets older and is
                    eating more solids, you will gradually increase the portion
                    sizes. Also, keep in mind that breast milk and/or infant
                    formula are providing for the total nutrition of your baby
                    at this stage.
                  </p>
