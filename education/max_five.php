                    <div class="det">
                        <b>​It's important to help your children develop a healthy attitude toward eating at an early age. By age three, children are less likely to use eating—or not eating—to be defiant. Generally, (although almost certainly not always), they will learn to better interact, participate, and enjoy family meals.  </b><br>
                        <h4 class="" style="color: green">5 Tips for Parents:</h4>

                      <p>
                        
                       <b> Accept strong preferences about foods.</b> Your three-year-old may be enthusiastic about eating, but he or she may have very specific food preferences. Some preferences may vary from day to day. For example, your child may gobble down a particular food one day, and then push away the same food the next day. She may ask for a certain food for several days in a row, and then insist that she doesn't like it anymore. As irritating as this behavior can feel, it is very typical for a three-year-old. It is best not to make an issue of it. Instead, continue to offer a variety of healthy foods, and let your child choose which of them and how much he or she will eat.<br>

                       <b> Encourage, but don't force trying new foods.</b> Offer very small amounts of a new food for your child to taste (an "adventure bite"), along with other foods he or she already likes. Do not expect your child to eat a full portion of an unfamiliar food.<br>

                       <b>Offer nutritious food choices at every meal.</b> As a parent, your job is to make sure that your three-year-old has nutritious food choices at every meal. After offering healthy options at the table, let him or her make the decision of how much to eat. If your child shows picky eating preferences—resisting vegetables, for example—don't get discouraged or frustrated. Keep offering a variety of healthy foods, even if your child did not like them before. Developing a taste for foods can require up to 15-20 repeated exposures. This is also an important time to establish healthy snacking and meal habits.<br>
                        
                       <b> Meals can be simple and nutritious.</b> Remember, meals at this age don't need to be fancy. In fact, most three-year-olds prefer simpler preparation. If you only a few minutes to prepare a meal, try simple meals that include a protein source, whole grain, fruit, vegetable, and dairy. For example, a turkey or peanut butter sandwich, a serving of carrots, an apple, and a glass of milk. A simple lunch like this takes less time to prepare than driving through a fast-food restaurant—and it is much healthier!<br>
                        
                       <b> Turn off the TV—especially at mealtimes.</b> Television advertising can be a big challenge to your three-year-old's good nutrition. Young children are easily influenced by ads for unhealthy foods like sugary cereals, fast food, and sweets. The best way to avoid this is put in place a "media curfew" at mealtime and bedtime, putting all devices away or plugging them into a charging station for the night.
