    <section class="our-services" id="6-12">
      <div class="row session-title">
        <h2>6–12 months</h2>
      </div>
      <div class="container">
        <div class="row">
          <div class="col-md-12 col-sm-12">
            <div class="service-box">
              <div class="d-flex">
                <div class="det">
                  <b
                    >After 6 months, breastmilk is still your baby’s main source
                    of energy and nutrients, but solid foods should now be
                    added.</b
                  ><br />
                  <b
                    >Your baby has a small stomach and needs to be eating small
                    amounts of soft nutritious food frequently throughout the
                    day. </b
                  ><br />
                  <b
                    >In addition to grains and tubers, feed your baby a variety
                    of foods – especially animal foods (dairy, eggs, meat, fish
                    and poultry), fruits and vegetables – every day.</b
                  ><br />
                  <b class="" style="color: green">Breastmilk continues</b>

                  <p>
                    At 6 months of age, breastmilk continues to be a vital
                    source of nutrition; but it’s not enough by itself. You need
                    to now introduce your baby to solid food, in addition to
                    breastmilk, to keep up with her growing needs. <br />

                    Be sure you give your baby her first foods after she has
                    breastfed, or between nursing sessions, so that your baby
                    continues to breastfeed as much as possible.<br />

                    When you start to feed your baby solid food, take extra care
                    that she doesn’t become sick. As she crawls about and
                    explores, germs can spread from her hands to her mouth.
                    Protect your baby from getting sick by washing your and her
                    hands with soap before preparing food and before every
                    feeding.
                  </p>
                  <b class="" style="color: green"
                    >Feeding your baby: 6–8 months old</b
                  >

                  <p>
                    From 6–8 months old, feed your baby half a cup of soft food
                    two to three times a day. Your baby can eat anything except
                    honey, which she shouldn't eat until she is a year old. You
                    can start to add a healthy snack, like mashed fruit, between
                    meals. As your baby gets increasing amounts of solid foods,
                    she should continue to get the same amount of breastmilk.
                  </p>
                  <b class="" style="color: green"
                    >Feeding your baby: 9–11 months old</b
                  >

                  <p>
                    From 9–11 months old, your baby can take half a cup of food
                    three to four times a day, plus a healthy snack. Now you can
                    start to chop up soft food into small pieces instead of
                    mashing it. Your baby may even start to eat food herself
                    with her fingers. Continue to breastfeed whenever your baby
                    is hungry.<br />

                    Each meal needs to be both easy for your baby to eat and
                    packed with nutrition. Make every bite count.<br />

                    Foods need to be rich in energy and nutrients. In addition
                    to grains and potatoes, be sure your baby has vegetables and
                    fruits, legumes and seeds, a little energy-rich oil or fat,
                    and – especially – animal foods (dairy, eggs, meat, fish and
                    poultry) every day. Eating a variety of foods every day
                    gives your baby the best chance of getting all the nutrients
                    he needs.
                    <br />
                    If your baby refuses a new food or spits it out, don’t force
                    it. Try again a few days later. You can also try mixing it
                    with another food that your baby likes or squeezing a little
                    breastmilk on top.
                  </p>
                  <b class="" style="color: green">
                    Feeding non-breastfed babies</b
                  >

                  <p>
                    If you're not breastfeeding your baby, she’ll need to eat
                    more often. She'll also need to rely on other foods,
                    including milk products, to get all the nutrition her body
                    needs.<br />

                    Start to give your baby solid foods at 6 months of age, just
                    as a breastfed baby would need. Begin with two to three
                    spoonfuls of soft and mashed food four times a day, which
                    will give her the nutrients she needs without breastmilk.<br />
                    From 6–8 months old, she’ll need half a cup of soft food
                    four times a day, plus a healthy snack. <br />
                    From 9–11 months old, she’ll need half a cup of food four to
                    five times a day, plus two healthy snacks.
                  </p>
